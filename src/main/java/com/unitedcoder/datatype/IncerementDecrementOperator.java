package com.unitedcoder.datatype;

public class IncerementDecrementOperator {
    public static void main(String[] args) {
        //increment  ++
        //decrement  --
        int i=100;
      //  System.out.println(i);
        i++;//post increment
     //   System.out.println(i);
        i++;
        i++;
      //  System.out.println(i);
        i--;//post decrement
        i--;
      //  System.out.println(i);
        ++i;//pre increment
        --i;//pre decrement
       // System.out.println(i);
        System.out.println(i+200);
    }
}
