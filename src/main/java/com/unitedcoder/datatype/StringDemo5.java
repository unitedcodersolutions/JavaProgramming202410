package com.unitedcoder.datatype;

public class StringDemo5 {
    public static void main(String[] args) {
        String name="United Coder";
        int year=2017;
        // %s is a format symbol for a string
        System.out.printf("The company name is %s",name);
        System.out.println(); //empty line
        System.out.printf("The company was created in %d",year);
    }
}
